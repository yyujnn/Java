package for문;

public class ex01for문기초 {

	public static void main(String[] args) {

		// for문 구조
		// 초기화 구문 검사조건 반복 후 작업
		// 1부터 10까지 출력
		// i =0 ; i<5; i++ --> 0,1,2,3,4

		for (int i = 1; i < 11; i++) {

			System.out.print(i + " ");
		}
		System.out.println();
		for (int i = 52; i > 20; i--) {
			System.out.print(i + " ");
		}

	}

}
